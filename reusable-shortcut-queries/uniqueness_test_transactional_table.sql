WITH cte as(
SELECT
  *,
  TO_JSON_STRING(`blala`) as full_row_json
FROM `blabla`
)
SELECT
  year,
  COUNT(*) AS total_count,
  COUNT(*) - COUNT(DISTINCT full_row_json) AS duplicate_count,
  CONCAT(ROUND(SAFE_DIVIDE(COUNT(DISTINCT full_row_json), COUNT(*)) * 100.0, 6),'%') AS distinct_pct,
FROM cte
GROUP BY ROLLUP(year)
ORDER BY GROUPING(year) DESC, year DESC;

